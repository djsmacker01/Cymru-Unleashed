function updateLanguage(lang) {
  // Update all elements with data-i18n or data-translate attribute
  document
    .querySelectorAll("[data-i18n], [data-translate]")
    .forEach((element) => {
      const key =
        element.getAttribute("data-i18n") ||
        element.getAttribute("data-translate");
      if (translations[lang][key]) {
        element.textContent = translations[lang][key];
      }
    });

  // Update active language in toggle
  const languageToggle = document.querySelectorAll(".language-toggle a");
  languageToggle.forEach((link) => {
    link.classList.remove("active");
    if (link.textContent.toLowerCase() === lang) {
      link.classList.add("active");
    }
  });

  // Store language preference
  localStorage.setItem("preferredLanguage", lang);
}

// Initialize language from localStorage or default to English
document.addEventListener("DOMContentLoaded", () => {
  const currentLang = localStorage.getItem("preferredLanguage") || "en";
  updateLanguage(currentLang);

  // Add click handlers to language toggle
  const languageToggle = document.querySelectorAll(".language-toggle a");
  languageToggle.forEach((link) => {
    link.addEventListener("click", (e) => {
      e.preventDefault();
      const lang = link.textContent.toLowerCase();
      updateLanguage(lang);
    });
  });
});
